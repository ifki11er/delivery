export const dictionaries: Record<Locale, Dictionary> = { ko, en, vi };

export type DictionaryKey = keyof typeof ko;

export function getDictionary(locale: string) {
  const dictionary = dictionaries[locale as Locale] ?? dictionaries.ko;

  return new Proxy(dictionary, {
    get(target, property) {
      if (typeof property !== "string") return undefined;
      return target[property] ?? dictionaries.ko[property] ?? property;
    },
  }) as Dictionary;
}
